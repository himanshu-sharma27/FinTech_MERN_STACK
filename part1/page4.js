//Arithmetic operators
console.log(10+5); // Addition
console.log(10-5);//+,-,*,/ etc

//Comparison operators
console.log(10>5);// <,<=,>=

console.log(10 == "10")//True because it only checks the value and not the type
console.log(10 === "10")//False because it checks both value and type

console.log(10 != "10")//False because it only checks the value and not the type and not equals is used
console.log(10 !== "10")//True because it checks both value and type and not equals to is used


//Assignment operators
let a =10;
a +=5;// a = a + 5; is the same as a+=5
a-=5;// a = a - 5; is the same as a-=5

//Logical operators
console.log(10>3)
console.log(10>3 && 10>15)//false

